
@testitem "funcao_g tests" begin
    using MEF_triangular, Test

    @test funcao_g([0.00, 0.00], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) == [1.00, 1.00]
    @test funcao_g([1.00, 0.00], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) == [5.00, 1.00]
    @test funcao_g([0.00, 1.00], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) == [1.00, 5.00]
    @test funcao_g([0.33, 0.33], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) ≈  [2.33, 2.33] atol=0.02

    @test funcao_g([0.00, 0.00], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) == [1.00, 2.00]
    @test funcao_g([1.00, 0.00], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) == [4.00, 1.00]
    @test funcao_g([0.00, 1.00], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) == [3.00, 4.00]
    @test funcao_g([0.33, 0.33], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) ≈  [2.66, 2.33] atol=0.02

    @test funcao_g([0.00, 0.00], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) == [1.40, 3.30]
    @test funcao_g([1.00, 0.00], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) == [2.70, 0.20]
    @test funcao_g([0.00, 1.00], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) == [7.80, 2.20]
    @test funcao_g([0.33, 0.33], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) ≈  [3.96, 1.90] atol=0.03
end

@testitem "derivada_funcao_g tests" begin
    using MEF_triangular, Test

    @test derivada_funcao_g(1, [2.33, 2.33], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) == [4.0,  0.0]
    @test derivada_funcao_g(1, [2.66, 2.33], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) == [3.0, -1.0]
    @test derivada_funcao_g(1, [3.96, 1.90], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) ≈  [1.3, -3.1]

    @test derivada_funcao_g(2, [2.33, 2.33], [1.0 5.0 1.0 ; 1.0 1.0 5.0]) == [0.0,  4.0]
    @test derivada_funcao_g(2, [2.66, 2.33], [1.0 4.0 3.0 ; 2.0 1.0 4.0]) == [2.0,  2.0]
    @test derivada_funcao_g(2, [3.96, 1.90], [1.4 2.7 7.8 ; 3.3 0.2 2.2]) ≈  [6.4, -1.1]

    @test_throws ErrorException derivada_funcao_g(3, [3.96, 1.90], [1.4 2.7 7.8 ; 3.3 0.2 2.2])
end